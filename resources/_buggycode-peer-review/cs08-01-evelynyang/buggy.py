def print_caffeine_level(amount):
   half_life = amount / 2
   return half_life

if __name__ == '__main__':
   six_hours = amount / 2
   print('After 6 hours: {:.2f}'.format(six_hours))
   twelve_hours = amount / 2 / 2
   print('After 12 hours: {:.2f}'.format(twelve_hours))
   day = amount / 2 / 2 / 2
   print('After a day: {:.2f}'.format(day))
